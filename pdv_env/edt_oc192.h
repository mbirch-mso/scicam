
#ifndef _EDT_OC192_H_
#define _EDT_OC192_H_


/*
* OC192 definitions.
*/

/* --reg_group OC192 */

#define	OC192_REF_CLOCK		0x0101002F
#define	OC192_XCVR_COM_PORT	0x01010031
#define	OC192_XCVR_CTL_STAT	0x01010032
#define	OC192_LIU_MDIO_BUS	0x01010033
#define	OC192_ENABLE		0x01010034
#define	OC192_LIU_STATUS	0x01010035
#define	OC192_FRAMING		0x01010037
#define	OC192_OUTPUT_DATA_SEL	0x0101003A
#define	OC192_CHAN_SELECT	0x0101004A

#define OC192_MERGE_RX_CTL 0x010100C0


#define	OC192_MEZZ_VERSION_STR	0x020100A0

/* redefined to not conflict with version_str */
#define OC192_DUAL_CHANNEL_MODE 0x010100A1


#define OC192_CNT_CTRL 0x010100D3
#define OC192_FIFO_STAT 0x010100D4
#define	OC192_RCV_FILTER	0x020100D5


/* single byte parts of oc192_demux_bitmask */

#define	OC192_DEMUX_BITMASK	0x020100D7
#define	OC192_DEMUX_BITMASK_LO	0x010100D7
#define	OC192_DEMUX_BITMASK_HI	0x010100D8
#define	OC192_DEMUX_MASK_ADDR	0x010100D9
#define	OC192_DEMUX_BITMASK_RD	0x020100DA
#define	OC192_DEMUX_BITMASK_RD_LO	0x010100DA
#define	OC192_DEMUX_BITMASK_RD_HI	0x010100DB

#define OC192_LOF_COUNT 0x020100DC
#define OC192_PAT_ERR_COUNT 0x020100DE

#define OC192_FALSE_FRAME_CNT 0x020100E4

#define OC192_TAG_CTRL		0x010100E8


#define	OC192_10GBE_BER_CNT 0x010100C8
#define	OC192_10GBE_DECODE_ERR_CNT 0x010100C9
#define	OC192_10GBE_LF_STATUS 0x010100CB
#define	OC192_10GBE_LLF_CNT 0x020100CC
#define	OC192_10GBE_RLF_CNT 0x020100CE
#define OC192_CH1_CNT_CTRL 0x010100D3
#define OC192_CH1_MEZZFIFO_STATUS 0x010100D4
#define OC192_10GBE_RXFILTER 0x020100D5
#define	OC192_10GBE_LPBK_CTRL 0x010100E8
#define	OC192_10GBE_PCS_CTRL 0x010100E9

/* modes for set */

#define EDT_OC192_LIU_NORMAL 0
#define EDT_OC192_LIU_LOOPBACK 1
#define EDT_OC192_LIU_BIST_RX 2
#define EDT_OC192_LIU_BIST_TX 3
#define EDT_OC192_LIU_BIST_TX_EXT 4
#define EDT_OC192_LIU_RESET 5
#define EDT_OC192_LIU_REMOTE_LPBK 6

/* --reg_bits OC192_REF_CLOCK */

#define OC192_REFCLK_OC 0x00
#define OC192_REFCLK_OTU2 0x01
#define OC192_REFCLK_10GBER 0x02
#define OC192_REFCLK_10GBER_FEC 0x03
#define OC192_REFCLK_125MHZ 0x04
#define OC192_REFCLK_ETH 0x05
#define OC192_REFCLK_OC_2 0x06
#define OC192_REFCLK_OTU1 0x07

/* --reg_bits OC192_XCVR_CTL_STAT */

#define TX_ENABLE 0x1
#define POWER_UP 0x4
#define LOS 0x10
#define MOD_NR 0x20
#define MOD_ABS 0x40
#define MOD_INTR 0x80

/* --reg_bits OC192_LIU_MDIO_BUS */

#define SDATA_IN	0x10
#define SDATA_OUT	0x1
#define SCLK		0x2
#define SDATA_TRI	0x8
#define SCLK_TRI	0x4

/* --reg_bits OC192_ENABLE */

#define AMCC_RST	0x1
#define TPLL_EN 0x2
#define OC192_INIT_EN 0x4
#define OC192_RAM_EN 0x8
#define RPLL_EN 0x10
#define SYS_LOCK 0x20 
#define OC192_TX_LOCKED 0x40
#define OC192_RX_LOCKED 0x80

/* --reg_bits OC192_LIU_STATUS */

#define RX_LOCKDET 0x1
#define TX_LOCKDET 0x2

/* --reg_bits OC192_FRAMING */

#define OC192_FRAME_SRCH 0x1
#define OC192_FRAME_EN 0x2
#define OC192_DISABLE_SCRAM 0x10
#define OC192_BIT_LOCK 0x20
#define OC192_BYTE_LOCK 0x40
#define OC192_FRAME_LOCK 0x80

/* --reg_bits OC192_FRAMING (alternate specific to 10GbE) */

#define OC192_10GBE_HI_BER 0x20
#define OC192_10GBE_SH_LOCK 0x40
#define OC192_10GBE_LINK_GOOD 0x80

/* --reg_bits OC192_OUTPUT_DATA_SEL */

#define OC192_PRBS7 0x1
#define OC192_PRBS_EN 0x8
#define OC192_HEADER_ON 0x10
#define OC192_TX_DDR_FIFO 0x20
#define OC192_RX_PRBS 0x80

/* --reg_bits OC192_OUTPUT_DATA_SEL (alternate specific to 10GbE*/
#define OC192_10GBE_TXDBG_PCS 0x2
#define OC192_10GBE_TXDBG_NOPCS 0x4
#define OC192_10GBE_TXDBG_MASK 0x6
#define OC192_10GBE_TX_DBG_PKT 0x40

/* --reg_bits OC192_CHAN_SELECT */

#define OC192_SEL 0x1

/* --reg_bits OC192_CNT_CTRL and OC192_CH1_CNT_CTRL */

#define COUNT_HOLD 0x1
#define EN_COUNTER 0x80

/* --reg_bits OC192_RCV_FILTER */
#define OC192_OVERHEAD_ONLY 0x1

/* --reg_bits OC192_TAG_CTRL */

#define OC192_TAG_ON	0x80
#define OC192_TAGS_ONLY	0x40
#define OC192_TAGS_XMIT 0x20

/* --reg_bits OC192_10GBE_LF_STATUS */

#define OC192_10GBE_LF_MASK 0x03
#define OC192_10GBE_LF 0x1
#define OC192_10GBE_RF 0x2
#define OC192_10GBE_RSVDF 0x3
#define OC192_10GBE_RT_RXLF 0x10
#define OC192_10GBE_RT_TXLF 0x20

/* --reg_bits OC192_10GBE_RXFILTER */

#define OC192_10GBE_DIS_IDLE_FILT 0x8

/* --reg_bits OC192_10GBE_LPBK_CTRL */

#define OC192_10GBE_LPBK_EN 0x1

/* --reg_bits OC192_10GBE_PCS_CTRL */

#define OC192_10GBE_DIS_DEC 0x1
#define OC192_10GBE_DIS_DESCRAM 0x2
#define OC192_10GBE_DIS_ENC 0x10
#define OC192_10GBE_DIS_SCRAM 0x20

/* -- */

#define OC192_TAG_MASK	0x3

#define OC192_FREQ_RX0 0
#define OC192_FREQ_RX1 1 
#define OC192_FREQ_TX0 2
#define OC192_FREQ_TX1 3

#define OC192_FREQ_MASK 0x30
#define OC192_FREQ_SHIFT 4


#endif
